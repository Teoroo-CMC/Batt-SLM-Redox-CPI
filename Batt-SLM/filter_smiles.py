def filter_smiles():

    from pathlib import Path
    from rdkit import Chem
    from rdkit.Chem import AllChem

    input_file_name = "Features"
    WORK_DIR = Path("your_own_path/Batt-SLM")

    smiles = []
    mols_with_hydrogen = []
    with open(WORK_DIR / f"{input_file_name}.csv", "r") as f:
        lines = f.readlines()
        titles = lines[0].strip("\n").split(";")
        smiles_col = titles.index("SMILES")
        smiles = [l.strip("\n").split(";")[smiles_col] for l in lines[1:]]
        mols_with_hydrogen = [Chem.AddHs(Chem.MolFromSmiles(smi)) for smi in smiles]

    # 
    solvent_reference_file = WORK_DIR / "KBS-FP-174.smi"
    print(solvent_reference_file)

    fingerprints_ref_solvents = []
    with open(solvent_reference_file, "r") as f:
        read_lines = f.readlines()
        for line in read_lines:
            cur_mol = Chem.AddHs(Chem.MolFromSmiles(line.split(" ")[0]))
            fingerprints_ref_solvents.append(AllChem.GetMorganFingerprintAsBitVect(cur_mol, 2, NUM_BIT_MORGAN))

    mol_scores = [0.0] * len(smiles)
    for mol_index in range(0, len(smiles)):

        print(mol_index)

        if not check_no_special_group(smiles[mol_index]):
            continue

        if not check_no_isomer(smiles[mol_index]):
            continue

        if not check_no_radical_neutral(mols_with_hydrogen[mol_index]):
            continue

        if not check_is_solvent_like(mols_with_hydrogen[mol_index]):
            continue

        if not check_is_suitable_to_battery(mols_with_hydrogen[mol_index], fingerprints_ref_solvents):
            continue

        mol_scores[mol_index] = 1.0

    # output file, skip the SbCl5 and DCE
    output_file = WORK_DIR / f"{input_file_name}-filters.csv"
    with open(output_file, "w") as g:
        g.write(lines[0].strip("\n") + ";PassFilters\n")
        for index in range(0, len(smiles)):
            g.write(lines[index+1].strip("\n") + ";%d\n"%(mol_scores[index]))

#
if __name__ == "__main__":

    filter_smiles()

