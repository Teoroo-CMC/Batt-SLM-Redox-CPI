def check_is_solvent_like(rdkit_mol):

    from rdkit import Chem
    from rdkit.Chem import Lipinski

    num_nonH = rdkit_mol.GetNumHeavyAtoms()
    if num_nonH < 3 or num_nonH > MAX_NUM_HEAVY_ATOMS:
        return False

    if Chem.rdMolDescriptors.CalcExactMolWt(rdkit_mol) > MAX_MOL_WEIGHT:
        return False

    is_valid_elem = True
    atom_symbols = [atom.GetSymbol() for atom in rdkit_mol.GetAtoms()]
    for symbol in atom_symbols:
        if symbol not in VALID_ELEMENTS:
            is_valid_elem = False
    if not is_valid_elem:
        return False

    num_aromatic_ring = Lipinski.NumAromaticRings(rdkit_mol)
    if num_aromatic_ring > MAX_NUM_AROMATIC_RING:
        return False

    return True

