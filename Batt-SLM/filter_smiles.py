def check_is_suitable_to_battery(rdkit_mol, fingerprints_ref_solvents):

    # at least one C atom, must include O/N atoms
    atom_symbols = [atom.GetSymbol() for atom in rdkit_mol.GetAtoms()]
    is_valid_elem = True
    is_with_need_elem = False
    is_with_optional_elem = False
    for symbol in atom_symbols:
        if symbol not in VALID_ELEMENTS:
            is_valid_elem = False
        elif symbol in NEEDED_ELEMENTS:
            is_with_need_elem = True
        elif symbol in OPTIONAL_ELEMENTS:
            is_with_optional_elem = True
    if not (is_valid_elem and is_with_need_elem and is_with_optional_elem):
        return False

    # check OH, NH, SH
    from rdkit.Chem import rdqueries
    queryO = rdqueries.AtomNumEqualsQueryAtom(8)
    oxygen_rdkit_atoms = rdkit_mol.GetAtomsMatchingQuery(queryO)

    is_aprotic = True
    for atom in oxygen_rdkit_atoms:
        neighbour_rdkit_atoms = atom.GetNeighbors()
        neighbour_rdkit_atoms_symbol = [neighbor.GetSymbol() for neighbor in neighbour_rdkit_atoms]
        if "H" in neighbour_rdkit_atoms_symbol:
            is_aprotic = False
            break

    queryN = rdqueries.AtomNumEqualsQueryAtom(7)
    nitrogen_rdkit_atoms = rdkit_mol.GetAtomsMatchingQuery(queryN)
    for atom in nitrogen_rdkit_atoms:
        neighbour_rdkit_atoms = atom.GetNeighbors()
        neighbour_rdkit_atoms_symbol = [neighbor.GetSymbol() for neighbor in neighbour_rdkit_atoms]
        if "H" in neighbour_rdkit_atoms_symbol:
            is_aprotic = False
            break

    if not is_aprotic:
        return False

    queryS = rdqueries.AtomNumEqualsQueryAtom(16)
    sulfur_rdkit_atoms = rdkit_mol.GetAtomsMatchingQuery(queryS)
    for atom in sulfur_rdkit_atoms:
        neighbour_rdkit_atoms = atom.GetNeighbors()
        neighbour_rdkit_atoms_symbol = [neighbor.GetSymbol() for neighbor in neighbour_rdkit_atoms]
        if "H" in neighbour_rdkit_atoms_symbol:
            is_aprotic = False
            break

    if not is_aprotic:
        return False

    if len(fingerprints_ref_solvents) > 0:
        from rdkit import DataStructs
        from rdkit.Chem import AllChem
        fingerprints = AllChem.GetMorganFingerprintAsBitVect(rdkit_mol, 2, NUM_BIT_MORGAN)
        smilarities = DataStructs.BulkTanimotoSimilarity(fingerprints, fingerprints_ref_solvents)
        max_simi = np.max(smilarities)
        if max_simi < MIN_SIMILARITY:
            return False

    return True

